            $('#fullpage').fullpage({
                sectionsColor: ['#fff', '#fff', '#fff', 'fff', '#fff', '#fff'],
                anchors: ['firstPage', 'secondPage', '3rdPage', '4thpage', '5thPage', 'lastPage'],
                navigation: true,
